function chooseEmotion(c, r){
    if (c == 1 || c == 2 || c == 18) {
        r = r % 6;
    } else if (c == 3 || c == 17) {
        r = r % 5;
    } else if (c == 12 || c == 13 || c == 14 || c == 15 || c== 16) {
        r = r % 4;
    }  else if (c == 4 || c == 5 || c == 6 || c == 7 || c== 11) {
        r = r % 3;
    }else{
        r= r % 2;
    }

    for (const x of emotionData) {
    if (x.col == c) {
        if (x.row == r) {
            return x.name;
        }
    }
}
    return undefined;
}


